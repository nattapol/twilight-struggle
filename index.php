<h1>Twilight Struggle</h1>
<h2>Introduction</h2>
<p><em>ขณะที่เสียงแตรแผดดังร้องเรียกพวกเราอีกครั้ง - แม้จะไม่ได้ร้องเรียกให้พวกเรากลับไปถืออาวุธ. แม้จะไม่มีการรบพุ่งให้เข้าไป เราก็ยังต้องเตรียมพร้อมเพื่อมัน - เราต้องพวกท่านเพื่อช่วยกันฝ่าฟันสถานการณ์นี้ไปด้วยกัน"</em><br />- John F. Kennedy, Inaugural Speech, มกรา 1961</p>
<p>ในปี 1945, เหล่าพันธมิตรได้ปราบเหล่าอสูรร้ายนาซีได้, ด้วยพายุเปลวเพลิงที่ทำให้จักรวรรดิ์ญี่ปุ่นต้องยอมสิโรราป. ได้เหลือยักษ์ใหญ่อยู่สองตน. </p>
